#String Operations
tester = "Sphinx of black quartz, judge my vow."

print(tester)
print("Character number 6: " + tester[5])
print("Characters from slot 4 up to 12:" + tester[4:12])
print("Phrase til slot 14: "+ tester[:14])
print("From 15 to end: " + tester[15:])
print("Length of string: " +str(len(tester)))
